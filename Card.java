public class Card {
	private String suit;
	private String face_value;
	private int point_value;
	
	//Suit methods
	public String getSuit() {
		return suit;
	}
	
	public void setSuit(String i) {
		suit = i;
	}
	
	//Face value methods
	public String getFaceValue() {
		return face_value;
	}
	
	public void setFaceValue(String f) {
		face_value = f;
	}
	
	//Face value methods
	public int getPointValue() {
		return point_value;
	}
	
	public void setPointValue(int f) {
		point_value = f;
	}
	
}
